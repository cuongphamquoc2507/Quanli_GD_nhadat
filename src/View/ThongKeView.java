    private void Tong() {
        DecimalFormat x = new DecimalFormat("###,###,###");
        int Tong = 0;

        for (int i = 0; i < tblbang1.getRowCount(); i++) {
            Tong += Float.parseFloat(tblbang1.getValueAt(i, 4).toString());

        }
        lblTong.setText("Tổng doanh thu: " + " " + x.format(Tong) + " " + "VND");
    }

