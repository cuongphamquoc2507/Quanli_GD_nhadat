    private void btntimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntimActionPerformed
        String Date = txttim.getText();
        if (Date.length() > 0) {
            try {
                giaodich = new GiaoDichDatDao().findbyDate(Date);
            } catch (Exception ex) {
                Logger.getLogger(ThongKeView.class.getName()).log(Level.SEVERE, null, ex);
            }
            tblModel.setRowCount(0);
            for (GiaoDichDat gd : giaodich) {
                tblModel.addRow(new Object[]{
                    tblbang1.getRowCount() + 1, gd.getMaGDD(), gd.getNgayGD(), gd.getMaDat(), gd.getThanhToan()
                });
            }
            Tong();
            tongso();
        }
    }//GEN-LAST:event_btntimActionPerformed

